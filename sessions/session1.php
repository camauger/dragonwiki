<h2>Session 1 - Gloradan</h2>
<hr>
<h4>10 janvier 2018</h4>
<p>Forteresse de Gloradan. C’est le printemps. Période agréable car les cols enneigés vont perdre leur blancheur et permettre plus facilement aux caravanes de marchands et d’explorateurs de venir à Gloradan.
</p>
<p>Les personnages, alors âgés d'une dizaine d'années, se trouvent en fin de journée sur les remparts de Gloradan. Ils s’amusent à faire voler un cerf-volant que leur a confectionner Melkor (vieux magicien pédagogue) sous le regard amical de Malsum (chevalier de Bahamut) et les froncements de sourcils de Vangrad d’Angoran (capitaine des gardes de Gloradan).
</p>
<p>Après une journée passer à étudier (l’école est obligatoire), ils sont contents de pouvoir jouer un peu.
</p>
<p>Le cor d’alarme se met à sonner et le bruit semble résonner à travers toute la vallée. Non, ce n’est pas un exercice.
</p>
<p>Par le col sud, on voit se dessiner une armée en marche. Mais la tour de garde n’a pas donner l’alerte, pourquoi?
</p>
<img src="../images/gloradan.jpg" alt="">
<p>La troupe s’approche. Ce sont des orcs, des mercenaires humains et... des géants de pierre! Ils sont menés par un cavalier chevauchant un cheval noir comme la nuit, à la crinière et aux sabots de feu, aux yeux rouges comme la braise, aux nasaux exhalant une fumée noirâtre. Le cavalier est entouré de grands corbeaux noirs, et à ses côtés avance ce qui semble être une grande panthère noire, le dos orné de tentacules qui fouettent l’air quand elle avance.
</p>
<p>Les magiciens se sentent soudain pris d’une sorte de vertige. Leur énergie magique vient d’être siphonnée par quelque chose, ou quelqu’un...
</p>
<p>Le cavalier vêtu de noir porte un grand bâton en os sculpté et orné de runes. Il enlève son capuchon, révélant le visage d’une elfe.
</p>
<p>
<em>- Dwinbar, crie la cavalière, je suis revenue, comme je te l’avais promis. Tu vas enfin comprendre qu’il ne fallait pas me sous-estimer. Je suis la porteuse du bâton des arcanes majeures et je vais détruire ta pathétique forteresse.
</em></p>
<p>Mais Dwinbar n’est pas là. Il est parti dans une de ses missions.
</p>
<p>
<em>
  - Si votre maître n’est pas là, tant mieux. J’ai hâte de voir l’expression de son visage lorsqu’il reviendra et qu’il ne trouvera ici que des ruines, et des cadavres.
</em></p>
<p>Branle-bas de combat sur les remparts, pendant que la troupe de Valindra s’apprête à prendre d’assaut la forteresse.
</p>
<p>Vangrad ordonne la tenue d’une réunion du conseil immédiatement. Les enfants ont l'audace d'aller discrètemement aller écouter ce qui se trame.
</p>
<p>Dwinbar est parti rendre visite à Chromatique, un mage excentrique qui vit de l’autre côté du Bois des Fées.
Impossible d’entrer en contact avec Dwinbar, car les magiciens ne peuvent plus faire appel à leurs pouvoirs.
Impossible de traverser le bois des Fées (il est interdit d’y entrer), mais l’interdiction s’applique aux adultes mais pas aux enfants.
Les enfants proposent courageusement d'aller trouver eux-mêmes Dwinbar.
</p>
<p>Vangrad leur apprend qu'ils peuvent sortir par un tunnel secret. Valindra le connaît probablement. Malsum et quelques archers protégent le départ des enfants.
Un groupe d’orcs sont dans les parages, prêts à couper des arbres pour le siège. Les enfants parviennent à leur échapper et se rendent rapidement jusqu'au Bois des Fées.</p>



