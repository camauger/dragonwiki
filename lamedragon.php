<p>Le Lame-Dragon Eladrin est un chevalier au service d'un puissant dragon métallique. Le lien psychique entre le chevalier et le dragon augmente au fur et à mesure que l'Eladrin accomplit des exploits.</p>
<h3>Abilités féériques</h3>

<h4>Vision dans le noir</h4>
Grâce à son sang elfique, l'Eladrin possède une vision supérieure dans l'obscurité. Il peut voir à 60 pieds dans une lumière faible comme avec une lumière vive, et dans le noir comme avec une lumière faible. Dans le noir, il ne dicerne pas les couleurs, uniquement des nuances de gris.

<h4>Ascendance féérique</h4>
L'Eladrin a l'avantage aux jets de sauvegarde contre les effets de charme et la magie ne peut pas l'endormir.

<h4>Disparition féérique</h4>
L'Eladrin peut lancer le sort Disparition Féérique. Il regagne cette abilité après chaque repos (court ou long).

<h3>Lame-Dragon</h3>

<h4>Maîtrises et compétences</h4>
Points de vie = 8 + modificateur de Constitution au niveau 1, +5 par niveau (+ modificateur de Constitution)
<br>Armures légères et moyennes, boucliers
<br>Armes simples et martiales
<br>Jets de sauvegarde : Sagesse et Charisme
<br>Compétences = 2 parmi les suivantes : Arcanes, Tromperie, Histoire, Intimidation, Investigation, Nature et Religion
<br>Sortilèges : 2 sorts mineurs (à volonté) et 1 sort niveau 1 (récupération à chaque repos court)
<br>Jet d'attaque de sort : 1d20 + bonus de Maîtrise + modificateur de Charisme
<br>Difficulté de sauvegarde : 8 + bonus de Maîtrise + modificateur de Charisme

<h3>Dragon Familier</h3>
Le Lame-Dragon possède un dragon familier (voir carte de sort).

<h3>Lame du Dragon</h3>
Après chaque long repos, le Lame-Dragon créer un lien avec une arme (pas une arme à deux mains).
Avec cette arme, il peut utiliser son modificateur de Charisme pour les jets d'attaque et de dégâts.

<h3>Proie du Dragon</h3>
[ Hexblade Curse ]
<br>Avec une action bonus, le Lame-Dragon une créature visible à moins de 30 pieds. La cible est "marquée" pendant une minute.
Le Lame-Dragon gagne les avantages suivants contre la cible :
<br>- Les dégâts infligés à la cible augmentent selon le bonus de maîtrise (1d4 au niveau 1).
<br>- Les jets d'attaque contre la cible sont des coups critiques sur un jet de 19 ou 20.
<br>- Si la cible est tuée, le Lame-Dragon regagne des points de vie (Niveau + modificateur de Charisme).
<br>Le Lame-Dragon regagne cette abilité après chaque repos (court ou long).
